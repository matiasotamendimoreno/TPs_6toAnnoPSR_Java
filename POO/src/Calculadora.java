
public class Calculadora {
	public int Sumar(int a, int b) {
		return a + b;
	}
	
	public int Sumar(int a, int b, int c) {
		return a + b + c;
	}
	
	public double Sumar (double a, double b, double c) {
		return a + b + c;
	}
	
	/*
	 * Pregunta 7.2:
	 * 		Sobre carga de metodos se refiere a la capacidad de que una clase contenga varios metodos con el mismo nombre, pero
	 * diferente lista de parametros. Permite que los metodos realicen tareas similares u relacionadas pero con diferentes datos
	 * de entrada.
	 */
}
